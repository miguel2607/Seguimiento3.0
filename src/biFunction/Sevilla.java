package biFunction;

public class Sevilla {
    private String nombre;
    private String liga;

    public Sevilla(String nombre, String liga) {
        this.nombre = nombre;
        this.liga = liga;
    }


    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getLiga() {
        return liga;
    }

    public void setLiga(String liga) {
        this.liga = liga;
    }
}
